    public void horarios() {
        List<Clase> clases = claseData.listarClases();
        for (int i = 9; i < 20; i++) {
            jCBHorarios.addItem(LocalTime.of(i, 0));
        }
        for (Clase clase1 : clases) {
            jCBHorarios.removeItem(clase1.getHorario());
        }
    }
